            What Does Root Canal Therapy Involve?
          </h2>
          <p>
            If the tissue or "nerve" in your tooth is inflamed and causing you severe discomfort or pain, Dr. Martin might need to perform root canal therapy.
            <br /><br />
            A root canal is a treatment used to repair and save a tooth that is badly decayed or becomes infected. If the tooth is infected antibiotics are usually prescribed to reduce the amount of infection. During a root canal procedure, the nerve and blood vessels are removed, the inside canal of the tooth is cleaned out and then sealed to protect from recurrent infection.
            <br /><br />
            Without treatment, the tissue surrounding the roots of the tooth will become infected and abscesses may form leading to the possible loss of the tooth. After a root canal has been completed, the structure becomes weakened and will require a crown in order to ensure longevity of the tooth.
